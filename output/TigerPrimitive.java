
/**
 * Valid primitive types in Tiger.
 */
public enum TigerPrimitive {
	/**
	 * An int value.
	 */
	INT,
	/**
	 * A fixedpt value.
	 */
	FIXEDPT,
	/**
	 * A one-dimensional array of int values.
	 */
	INT_ARRAY,
	/**
	 * A one-dimensional array of fixedpt values.
	 */
	FIXEDPT_ARRAY,
	/**
	 * A two-dimensional array of int values.
	 */
	INT_2D_ARRAY,
	/**
	 * A two-dimensional array of fixedpt values.
	 */
	FIXEDPT_2D_ARRAY;
}
