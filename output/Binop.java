/**
 * A Binop holds a list of valid binary operators.
 */
public enum Binop {
	/**
	 * &
	 */
	AND,
	/**
	 * |
	 */
	OR,
	/**
	 * -
	 */
	MINUS,
	/**
	 * +
	 */
	PLUS,
	/**
	 * *
	 */
	MULT,
	/**
	 * /
	 */
	DIV,
	/**
	 * =
	 */
	EQUAL,
	/**
	 * <>
	 */
	NOT_EQUAL,
	/**
	 * <
	 */
	LESS_THAN,
	/**
	 * >
	 */
	GREATER_THAN,
	/**
	 * <=
	 */
	LESS_THAN_OR_EQUAL,
	/**
	 * >=
	 */
	GREATER_THAN_OR_EQUAL
}
